		<h2 style="margin: auto 15rem;">Entonces, ¿qué nos revela Spotify sobre el consumo de música actual?</h2>
		<p style="margin: 1.5rem 5rem;">
			En 2023, el top 50 de Spotify destaca por el dominio del pop, con el K-Pop y el urbano latino ganando terreno rápidamente. Las canciones más populares se caracterizan por un contenido accesible y universal, generalmente no explícito. A pesar de la diversidad de géneros, predominan los artistas de EE.UU. y Reino Unido, reflejando un dominio del inglés en la escena musical global. La desigualdad de género persiste con un 76% de artistas masculinos, mientras que las bandas, aunque menos frecuentes, aportan una rica variedad de estilos. En resumen, 2023 muestra una diversidad musical dinámica, pero ¿cómo podemos ampliar aún más la representación cultural y de género en la música?
